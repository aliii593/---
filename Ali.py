# Сложение
a = 5 + 3
print("Сложение (5 + 3):", a)

# Вычитание
b = 10 - 4
print("Вычитание (10 - 4):", b)

# Умножение
c = 6 * 7
print("Умножение (6 * 7):", c)

# Возведение в степень
d = 2 ** 3
print("Возведение в степень (2 ** 3):", d)

# Деление
e = 15 / 4
print("Деление (15 / 4):", e)

# Целочисленное деление
f = 15 // 4
print("Целочисленное деление (15 // 4):", f)

# Остаток от деления
g = 15 % 4
print("Остаток от деления (15 % 4):", g)
